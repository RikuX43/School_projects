import math
pi = math.pi
radius = float(input('Radius: '))

Diameter = 2 * radius
print("Diameter: ", Diameter)

Circumference = Diameter * pi

print("Circumference : ", Circumference)

area = 4 * pi * radius ** 2

print ("Surface area: ", area)

volume = 4/3 * pi * radius ** 3

print("Volume : ", volume)
